const PADDING_NAME = "ppt/padding.bin";

function fixedEntries(): ZipEntry[] {
  return [
    { name: "[Content_Types].xml", data: utf8(CONTENT_TYPES) },
    { name: "_rels/.rels", data: utf8(ROOT_RELS) },
    { name: "ppt/presentation.xml", data: utf8(PRESENTATION_XML) },
    {
      name: "ppt/_rels/presentation.xml.rels",
      data: utf8(PRESENTATION_RELS),
    },
    { name: "ppt/slides/slide1.xml", data: utf8(SLIDE_XML) },
    { name: "ppt/slides/_rels/slide1.xml.rels", data: utf8(SLIDE_RELS) },
    {
      name: "ppt/slideLayouts/slideLayout1.xml",
      data: utf8(SLIDE_LAYOUT_XML),
    },
    {
      name: "ppt/slideLayouts/_rels/slideLayout1.xml.rels",
      data: utf8(SLIDE_LAYOUT_RELS),
    },
    {
      name: "ppt/slideMasters/slideMaster1.xml",
      data: utf8(SLIDE_MASTER_XML),
    },
    {
      name: "ppt/slideMasters/_rels/slideMaster1.xml.rels",
      data: utf8(SLIDE_MASTER_RELS),
    },
    { name: "ppt/theme/theme1.xml", data: utf8(THEME_XML) },
  ];
}

/**
 * 目標バイト数ちょうどになる最小有効 PPTX を生成する。
 * 余りは ppt/padding.bin（store）で埋める。
 */
export function generatePptx(targetBytes: number): Uint8Array {
  return generatePaddedOoxml(targetBytes, fixedEntries(), PADDING_NAME, "PPTX");
}
